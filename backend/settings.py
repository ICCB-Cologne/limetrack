def load_env_file() -> str:
    output = ""
    env_dir = "env"
    dev_path = os.path.join(env_dir, "development.env")
    prod_path = os.path.join(env_dir,"production.env")

    if os.path.exists(prod_path):
        output = prod_path

    else:
        output = dev_path

    return output


